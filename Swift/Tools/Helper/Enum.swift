import Foundation

// 日期格式枚举
public enum DateStyle: String {
    case defaultStyle = "yyyy-MM-dd HH:mm:ss"
    case style1 = "yyyy-MM-dd HH:mm"
    case stype2 = "yyyy-MM-dd hh:mm:ss"
    case style3 = "yyyy/MM/dd HH:mm:ss"
    case style4 = "yyyy/MM/dd HH:mm"
}
